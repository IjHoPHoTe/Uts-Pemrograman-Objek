
package LuasDanKelilingPersegi;

public class LuasDanKelilingPersegi {

    public static void main(String[] args) {
        int sisi;
        int hasilkeliling;
        int hasilluas;
        
        sisi = 10;
        
        hasilkeliling = sisi * sisi * sisi * sisi;
        hasilluas = sisi * sisi;
        
        System.out.println ("Hasil Keliling Persegi Adalah = " + hasilkeliling);
        System.out.println ("Hasil Luas Persegi Adalah = " + hasilluas);
        
    }
    
}
